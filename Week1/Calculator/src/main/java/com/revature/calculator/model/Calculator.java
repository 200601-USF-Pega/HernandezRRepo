package com.revature.calculator.model;

public class Calculator {

	public int  multiply(int a, int b) {
		int product = a * b;
		return product;
	}

	public int  divide(int a, int b) {
		int quotient = a / b;
		return quotient;
	}

	public int  add(int a, int b) {
		int sum = a + b;
		return sum;
	}

	public int  subtract(int a, int b) {
		int difference = a - b;
		return difference;
	}
}
